package com.best.web.model.order;

public class SupplierInfo {
	
	private String supplierServiceId;
	private String supplierId;
	private String supplierName;
	private String supplierAbbr;
	private String intro;
	
	public String getSupplierServiceId() {
		return supplierServiceId;
	}
	public void setSupplierServiceId(String supplierServiceId) {
		this.supplierServiceId = supplierServiceId;
	}
	public String getSupplierId() {
		return supplierId;
	}
	public void setSupplierId(String supplierId) {
		this.supplierId = supplierId;
	}
	public String getSupplierName() {
		return supplierName;
	}
	public void setSupplierName(String supplierName) {
		this.supplierName = supplierName;
	}
	public String getSupplierAbbr() {
		return supplierAbbr;
	}
	public void setSupplierAbbr(String supplierAbbr) {
		this.supplierAbbr = supplierAbbr;
	}
	public String getIntro() {
		return intro;
	}
	public void setIntro(String intro) {
		this.intro = intro;
	}
	
	
}
