package com.best.web.model.cust;

public class CustBaseInfo {
	private String custId;
	private String name;
	private String sex;
	private String birthday;
	private String cretType;
	private String cretNo;
	private String mobile;
	private String mail;
	
	public String getCustId() {
		return custId;
	}
	public void setCustId(String custId) {
		this.custId = custId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	
	public String getCretType() {
		return cretType;
	}
	public void setCretType(String cretType) {
		this.cretType = cretType;
	}
	public String getCretNo() {
		return cretNo;
	}
	public void setCretNo(String cretNo) {
		this.cretNo = cretNo;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	
}
